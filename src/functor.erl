-module(functor).

%% API
-export([fmap/2, '<$>'/2]).

-type functor(_F, _A) :: any().

-callback fmap(fun((A) -> B), functor(F, A)) -> functor(F, B).
%%%===================================================================
%%% API
%%%===================================================================

fmap(F, FA) ->
    FModule = typeclass:module(functor, FA),
    FModule:fmap(F, FA).

'<$>'(F, FA) ->
    fmap(F, FA).
